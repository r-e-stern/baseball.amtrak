function Station(x,y,n){
    this.long = x;
    this.lat = y;
    this.name = n;
    this.distanceTo= function (a,b){
        return latLongDistForm(this.lat, this.long, a, b);
    };
}

function latLongDistForm(lat1, lon1, lat2, lon2){
    var R = 6371e3;
    var φ1 = lat1*Math.PI/180;
    var φ2 = lat2*Math.PI/180;
    var Δφ = (lat2-lat1)*Math.PI/180;
    var Δλ = (lon2-lon1)*Math.PI/180;
    var a = Math.sin(Δφ/2) * Math.sin(Δφ/2) +
        Math.cos(φ1) * Math.cos(φ2) *
        Math.sin(Δλ/2) * Math.sin(Δλ/2);
    var c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
    return R * c;
    //output in meters
}

function formatM(n){
    if(n>=1000){
        n/=1000;
        return n.toPrecision(Math.floor(Math.log10(n))+2)+" km";
    }else{
        return n.toPrecision(Math.floor(Math.log10(n))+2)+" m";
    }
}

function Stadium(x,y,n,l){
    this.long = x;
    this.lat = y;
    this.name = n;
    this.level = l;
    this.closestStation = function(array){
        var min = array[0].distanceTo(this.lat,this.long);
        var minInd = 0;
        for(var i=1; i<array.length; i++){
            if(min>array[i].distanceTo(this.lat,this.long)){
                var min = array[i].distanceTo(this.lat,this.long);
                var minInd = i;
            }
        }
        return array[minInd];
    };
    this.distanceToClosestStation = function(){
        return this.closestStation(stations).distanceTo(this.lat,this.long);
    };
    this.table = function(){
      return "<tr><td>"
          +this.name+"</td><td>"
          +this.level+"</td><td>"
          +this.closestStation(stations).name+"</td><td>"
          +formatM(this.distanceToClosestStation())+"</td></tr>";
    };
    this.levelN = function(){
        if(this.level=="MLB"){
            return 6;
        }else if(this.level=="AAA"){
            return 5;
        }else if(this.level=="AA"){
            return 4;
        }else if(this.level=="A"){
            return 3;
        }else if(this.level=="A (short-season)"){
            return 2;
        }else if(this.level=="Rookie"){
            return 1;
        }else{
            return 0;
        }
    };
}

